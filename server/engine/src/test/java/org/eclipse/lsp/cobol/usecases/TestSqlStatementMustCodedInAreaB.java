package org.eclipse.lsp.cobol.usecases;

import com.google.common.collect.ImmutableList;
import com.google.common.collect.ImmutableMap;
import org.eclipse.lsp.cobol.service.delegates.validations.SourceInfoLevels;
import org.eclipse.lsp.cobol.usecases.engine.UseCaseEngine;
import org.eclipse.lsp4j.Diagnostic;
import org.eclipse.lsp4j.DiagnosticSeverity;
import org.eclipse.lsp4j.Range;
import org.junit.jupiter.api.Test;

/** This test checks if SQL statements coded in Area B */
class TestSqlStatementMustCodedInAreaB {
  private static final String TEXT =
      "       IDENTIFICATION DIVISION.\n"
          + "       PROGRAM-ID. HELLO-SQL.\n"
          + "       DATA DIVISION.\n"
          + "       WORKING-STORAGE SECTION.\n"
          + "       PROCEDURE DIVISION."
          + "              EXEC SQL\n"
          + "         {SELECT|1} * FROM EMP;\n"
          + "         {WHERE|2} ID = 0;\n"
          + "              END-EXEC.\n";

  @Test
  void test() {
    UseCaseEngine.runTest(
        TEXT,
        ImmutableList.of(),
        ImmutableMap.of(
            "1",
            new Diagnostic(
                new Range(),
                "The following token must start in Area B: SELECT",
                DiagnosticSeverity.Warning,
                SourceInfoLevels.WARNING.getText()),
            "2",
            new Diagnostic(
                new Range(),
                "The following token must start in Area B: WHERE",
                DiagnosticSeverity.Warning,
                SourceInfoLevels.WARNING.getText())));
  }
}
