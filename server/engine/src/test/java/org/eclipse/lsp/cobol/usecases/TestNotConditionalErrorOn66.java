package org.eclipse.lsp.cobol.usecases;

import com.google.common.collect.ImmutableList;
import com.google.common.collect.ImmutableMap;
import org.eclipse.lsp.cobol.service.delegates.validations.SourceInfoLevels;
import org.eclipse.lsp.cobol.usecases.engine.UseCaseEngine;
import org.eclipse.lsp4j.Diagnostic;
import org.eclipse.lsp4j.DiagnosticSeverity;
import org.eclipse.lsp4j.Range;
import org.junit.jupiter.api.Test;

/** This test checks that 66 is not allowed as a conditional data name */
class TestNotConditionalErrorOn66 {
  private static final String TEXT =
      "       IDENTIFICATION DIVISION.\n"
          + "       PROGRAM-ID. TESTREPL.\n"
          + "       DATA DIVISION.\n"
          + "       WORKING-STORAGE SECTION.\n"
          + "       01  {$*PARENT}.\n"
          + "           02 {$*CHILD} PIC 9.\n"
          + "       66 {$*PARENT1} RENAMES {$CHILD}.\n"
          + "          88 {$*COND|1} VALUE 0.\n"
          + "       PROCEDURE DIVISION.\n"
          + "           GOBACK.  ";

  @Test
  void test() {
    UseCaseEngine.runTest(
        TEXT,
        ImmutableList.of(),
        ImmutableMap.of(
            "1",
            new Diagnostic(
                new Range(),
                "A conditional data item is not found for this condition: COND",
                DiagnosticSeverity.Error,
                SourceInfoLevels.ERROR.getText())));
  }
}
